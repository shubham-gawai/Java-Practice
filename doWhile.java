
public class doWhile {
	public static void main(String args[]) {
		int x =1;
		
		do {
			System.out.println(x);
			x++;
		} while(x<5);
		
		// do while statement program basic for understanding :
		// even in the false condition the code will run once : 
		//here if the condition is false the code will run once and prints 1 : 
		
	}
}
